public class HelloTypes {
    public static void main(String[] args) {
        int entero = 10;
        double decimal = 10.5;
        char caracter = 'c';
        String texto = "hola mundo";
        boolean bandera = true;

        System.out.println("Este es un numero entero: " + entero);
        System.out.println("Este es un numero decimal: " + decimal);
        System.out.println("Este es un caracter: " + caracter);
        System.out.println("Este es una cadena de caracteres: " + texto);
        System.out.println("Este es un valor booleano: " + bandera);
    }
}
